package com.gsitm.air.vo;

public class StatsByTimelineVO {
	
	private String arrPassenger;
	private String depPassenger;
	private String passenger;
	private String time;
	
	public String getArrPassenger() {
		return arrPassenger;
	}
	public void setArrPassenger(String arrPassenger) {
		this.arrPassenger = arrPassenger;
	}
	public String getDepPassenger() {
		return depPassenger;
	}
	public void setDepPassenger(String depPassenger) {
		this.depPassenger = depPassenger;
	}
	public String getPassenger() {
		return passenger;
	}
	public void setPassenger(String passenger) {
		this.passenger = passenger;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
}
